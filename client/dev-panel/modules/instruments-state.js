// Этот модуль управляет состоянием (данными) для вкладки "Реестр оборудования".
// Он предоставляет централизованное хранилище для списка оборудования,
// чтобы другие модули могли получать к нему доступ и обновлять его.

/**
 * @type {Array<object>}
 * Хранит текущий список всех записей об оборудовании, загруженный с сервера.
 */
export let allInstruments = [];

/**
 * Обновляет глобальное состояние списка оборудования.
 * @param {Array<object>} data - Новый массив с данными об оборудовании.
 */
export function setInstruments(data) {
    allInstruments = data;
}