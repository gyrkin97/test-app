// Этот файл содержит глобальные константы, используемые в разных частях клиентского приложения,
// чтобы избежать "магических строк" и обеспечить единый источник правды для ключей хранения.

/**
 * Ключ для хранения прогресса активного (незавершенного) теста в localStorage.
 * Позволяет восстановить сессию теста при перезагрузке страницы.
 * @type {string}
 */
export const ACTIVE_TEST_PROGRESS_KEY = 'activeTestProgress';

/**
 * Ключ для временного хранения ID результата, ожидающего ручной проверки, в sessionStorage.
 * Используется для отображения экрана ожидания после завершения теста с открытыми вопросами.
 * @type {string}
 */
export const PENDING_RESULT_SESSION_KEY = 'pendingResult';

/**
 * Ключ для хранения данных последнего полностью завершенного теста в текущей сессии (sessionStorage).
 * Позволяет показать экран с результатами при перезагрузке страницы.
 * @type {string}
 */
export const LAST_RESULT_SESSION_KEY = 'lastResultData';

/**
 * Ключ для хранения ФИО пользователя между сессиями в sessionStorage.
 * Позволяет избежать повторного ввода имени при возвращении на страницу выбора тестов.
 * @type {string}
 */
export const USER_FIO_SESSION_KEY = 'userFIO';

/**
 * Ключ для хранения ID активного теста в sessionStorage.
 * Используется для быстрого доступа к ID текущего теста без загрузки всего прогресса.
 * @type {string}
 */
export const ACTIVE_TEST_SESSION_KEY = 'activeTestId';