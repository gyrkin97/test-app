// --- ФАЙЛ: client/modules/utils/constants.js (ПОЛНАЯ ИТОГОВАЯ ВЕРСИЯ) ---
// Этот файл содержит константы - ключи, используемые для хранения данных
// в localStorage и sessionStorage, чтобы избежать опечаток и обеспечить
// централизованное управление ими.

// --- Ключи для localStorage ---
// Данные в localStorage сохраняются между сессиями браузера.

// Хранит прогресс теста, который еще не был завершен (например, если пользователь закрыл вкладку).
// К этому ключу будет добавляться ID теста (например, 'currentTestProgress_uuid-123').
export const ACTIVE_TEST_PROGRESS_KEY = 'currentTestProgress';


// --- Ключи для sessionStorage ---
// Данные в sessionStorage удаляются при закрытии вкладки или окна браузера.

// Хранит ФИО пользователя для текущей сессии, чтобы ему не приходилось вводить его заново
// при переходе между экранами.
export const FIO_SESSION_KEY = 'userFIO';

// Хранит ID теста, который в данный момент активен (проходится пользователем).
// Используется для восстановления сессии при перезагрузке страницы.
export const ACTIVE_TEST_SESSION_KEY = 'activeTestId';

// Хранит данные последнего ПОЛНОСТЬЮ ЗАВЕРШЕННОГО теста для отображения при возврате на страницу
// в рамках одной сессии.
export const LAST_RESULT_SESSION_KEY = 'lastTestResult';

// Хранит информацию о результате, который был отправлен, но ожидает ручной проверки администратором.
// Это позволяет показать пользователю экран ожидания даже после перезагрузки страницы.
export const PENDING_RESULT_SESSION_KEY = 'pendingTestResult';